    private void bttnLoginAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnLoginAceptarActionPerformed
        String usuario = fieldLoginUsuario.getText();
        String password = fieldLoginClave.getText();

        empleado = gestorHotel.login(usuario, password);

        if (empleado != null) {
            if (empleado.getClass().getSimpleName().equals(Recepcionista.class.getSimpleName())) {
                VentanaRecepcionista ventanaRecepcionista = new VentanaRecepcionista((Recepcionista) empleado, gestorHotel, this);
                ventanaRecepcionista.setLocationRelativeTo(null);
                ventanaRecepcionista.setVisible(true);
                setVisible(false);
            } else if (empleado.getClass().getSimpleName().equals(Supervisor.class.getSimpleName())) {
                VentanaSupervisor ventanaSupervisor = new VentanaSupervisor((Supervisor) empleado, gestorHotel, this);
                ventanaSupervisor.setLocationRelativeTo(null);
                ventanaSupervisor.setVisible(true);
                setVisible(false);
            } else if (empleado.getClass().getSimpleName().equals(Propietario.class.getSimpleName())) {
                VentanaPropietario ventanaPropietario = new VentanaPropietario((Propietario) empleado, this);
                ventanaPropietario.setLocationRelativeTo(null);
                ventanaPropietario.setVisible(true);
                setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usuario y/o contraseña incorrecta", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        fieldLoginClave.setText("");
        fieldLoginUsuario.setText("");
    }//GEN-LAST:event_bttnLoginAceptarActionPerformed

