    @Override
    public void update(Observable o, Object arg) {
        DetallesReserva detallesReserva = (DetallesReserva) arg;
        String info
                = "La reserva del cliente con DNI " + detallesReserva.getDNI() + " no se ha cumplido su plazo de entrada."
                + "\nDatos Reserva :\n"
                + "\tFecha de entrada : " + detallesReserva.getFech_ent() + "\n"
                + "\tImporte cobrado de una noche :" + detallesReserva.getImporte() + "€\n"
                + "\tNumero de dias : " + detallesReserva.getNum_dias() + "\n"
                + "\tCliente : " + detallesReserva.getNombre() + "\n"
                + "\tTelefono : " + detallesReserva.getTelefono() + "\n"
                + "\tDireccion : " + detallesReserva.getDireccion();

        JOptionPane.showMessageDialog(this, info,
                "Reserva Caducada", JOptionPane.WARNING_MESSAGE);
    }

