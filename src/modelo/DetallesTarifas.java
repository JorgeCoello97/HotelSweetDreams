package modelo;

public class DetallesTarifas {

    public float precioSimple;
    public float precioDoble;
    public float precioMatrimonio;

    public DetallesTarifas(float precioSimple, float precioDoble, float precioMatrimonio) {
        this.precioSimple = precioSimple;
        this.precioDoble = precioDoble;
        this.precioMatrimonio = precioMatrimonio;
    }

}
